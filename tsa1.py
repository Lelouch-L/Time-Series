import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
testdate=pd.date_range('2020/03/16',periods=10,freq='5D')
print(testdate)
pd.set_option('display.width',1000)
pd.set_option('display.max_rows',1000)
pd.set_option('display.max_columns',500)
time=pd.Series(np.random.randn(20),
               index=pd.date_range('2020-3-16',periods=20,freq='2D'))
print(time)
print(time.truncate(before='2020-4-11'))
print(time.truncate(after='2020-4-11'))
print(pd.date_range('2020-3-16','2020-4-5',freq='D'))
print(pd.period_range('2020-3-16 10:00',periods=10,freq='25H'))
print(pd.Period('2020-3-16 10')+pd.Timedelta('1D'))
print(pd.Timestamp('2020-3-17 10')+pd.Timedelta('15ns'))
print(pd.Series(np.random.randn(10),index=testdate))
print(time.resample('3D').sum())
print(time.resample('3D').mean())
print(time.resample('D').asfreq())
print(time.resample('D').bfill(1))
print(time.resample('D').interpolate('linear'))
df=pd.date_range('2020-3-16',periods=600,freq='D')
data=pd.Series(np.random.randn(600),index=df)
print(data.head())
r=data.rolling(window=10)
print(r.mean())
plt.figure(figsize=(15, 5))
data.plot()
r.mean().plot()
plt.show()